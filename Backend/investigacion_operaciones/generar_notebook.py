    "$$\\hat{y}_{patron} = \\sigma\\left(W_c \\cdot h_{backbone} + b_c\\right), \\quad \\sigma(x) = \\frac{1}{1+e^{-x}}$$\n",
    "\n",
    "Cada neurona de salida representa UNA patologia (no son mutuamente excluyentes):\n",
    "$$\\hat{p}_{ic} \\in [0,1] \\quad \\text{para } c = 1, \\ldots, 15 \\text{ clases de patologia}$$\n",
