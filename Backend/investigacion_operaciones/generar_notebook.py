    "$$\\boxed{x_{l+1} = H_l(x_l) + x_l}$$\n",
    "\n",
    "La conexion de salto resuelve el problema de **vanishing gradient** en redes profundas:\n",
    "$$\\frac{\\partial \\mathcal{L}}{\\partial x_l} = \\frac{\\partial \\mathcal{L}}{\\partial x_L}\\prod_{i=l}^{L-1}\\left(1 + \\frac{\\partial H_i}{\\partial x_i}\\right)$$\n",
    "\n",
    "El termino $+1$ garantiza que el gradiente nunca se anule completamente.\n",
    "\n",
    "**Modulo CBAM — Atencion de Canal:**\n",
    "$$M_c(F) = \\sigma\\left(W_1\\left(\\text{ReLU}\\left(W_0 \\cdot \\text{AvgPool}(F)\\right)\\right) + W_1\\left(\\text{ReLU}\\left(W_0 \\cdot \\text{MaxPool}(F)\\right)\\right)\\right)$$\n",
    "\n",
    "**Modulo CBAM — Atencion Espacial:**\n",
    "$$M_s(F') = \\sigma\\left(f^{7\\times7}\\left([\\text{AvgPool}(F'); \\text{MaxPool}(F')]\\right)\\right)$$\n",
    "\n",
    "**Salida CBAM:**\n",
    "$$F'' = M_s(M_c(F) \\otimes F) \\otimes (M_c(F) \\otimes F)$$\n",
    "\n",
    "El modulo CBAM aprende **donde** y **que** mirar en la imagen — critico para localizar anomalias fetales.\n",
    "\n",
    "### Que hace exactamente\n",
    "- Extrae features con ResNet-50 (50 capas, 25.6M parametros)\n",
    "- Aplica atencion de canal: prioriza los filtros mas relevantes\n",
    "- Aplica atencion espacial: focaliza en regiones anatomicas criticas\n",
